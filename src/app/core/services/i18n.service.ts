@Injectable({
  providedIn: 'root'
})
export class I18nService {
  private readonly languageSubject = new BehaviorSubject<AppLanguage>(this.readLanguage());
  readonly language$ = this.languageSubject.asObservable();

  getCurrentLanguage(): AppLanguage {
    return this.languageSubject.value;
  }

  setLanguage(language: AppLanguage): void {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    this.languageSubject.next(language);
  }

  translate(key: string, params?: Record<string, string | number>): string {
    const language = this.getCurrentLanguage();
    const dictionary = translations[language] ?? translations.es;
    const fallback = translations.es[key] ?? key;
    let value = dictionary[key] ?? fallback;

    if (!params) {
      return value;
    }

    Object.entries(params).forEach(([paramKey, paramValue]) => {
      value = value.replace(new RegExp(String.raw`\{\{${paramKey}\}\}`, 'g'), String(paramValue));
    });

    return value;
  }

  private readLanguage(): AppLanguage {
    const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY) as AppLanguage | null;
    if (stored === 'es' || stored === 'en' || stored === 'pt') {
      return stored;
    }
    return 'es';
  }
}
